package Operators.Problems;

public class CompareIntegers {
	public static void main(String[] args) {
        int x = 15, y = 20;

        if (x > y) {
            System.out.println(x + " is greater.");
        } else if (y > x) {
            System.out.println(y + " is greater.");
        } else {
            System.out.println("Both are equal.");
        }
    }

}
